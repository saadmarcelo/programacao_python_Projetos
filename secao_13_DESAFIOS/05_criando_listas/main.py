num1 = float(input("Entre com o 1 numero: "))
num2 = float(input("Entre com o 2 numero: "))

soma = num1 + num2
subtracao = num1 - num2
multi = num1 * num2
divisao = num1 / num2
exp = num1**num2

print(f"O valor da soma do {num1} + {num2} = {soma}")
print(f"O valor da subtracao do {num1} - {num2} = {subtracao}")
print(f"O valor da multiplicacao do {num1} * {num2} = {multi}")
print(f"O valor da divisao do {num1} / {num2} = {divisao}")
print(f"O valor da exponenciacao do {num1} ** {num2} = {exp}")
